package in.com.rays;

public class SwitchCase {
 public static void main(String[] args) {
	int main=4;
	switch(main) {
	case 1:
		System.out.println("1");
		break;
	case 2:
		System.out.println("2");
		break;
	case 3:
		System.out.println("3");
		break;
	case 4:
		System.out.println("4");
        break;
        default:
        	System.out.println("Not Found");
        	break;
	}
	
	}
}


